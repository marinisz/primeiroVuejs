<template>
  <div class="container">
    <h1>Comentarios</h1>
    <div class="form-todo form-group">
      <p>
        <input
          type="text"
          placeholder="nome"
          name="author"
          class="form-control"
          v-model="name"
        />
      </p>
      <p>
        <textarea
          name="message"
          placeholder="ComentÃ¡rio"
          class="form-control"
          v-model="message"
        ></textarea>
      </p>
      <button v-on:click="addComment" type="submit" class="btn btn-primary">
        Comentar
      </button>
    </div>
    <div class="list-group">
      <div class="list-group-item my-2 text-start" v-for="(comment, index) in allComments" :key="comment.id">
        <span class="comment__author"
          >Autor: <strong>{{ comment.name }}</strong></span
        >
        <p>{{ comment.message }}</p>
        <div>
          <a
            href="#"
            title="Excluir"
            v-on:click.prevent="deleteComment(index)"
            class="btn btn-secondary"
            >Excluir</a
          >
        </div>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      comments: [],
      name: "",
      message: "",
    };
  },
  methods: {
    addComment() {
      if (this.message.trim() === "") {
        return;
      } else {
        this.comments.push({
          name: this.name,
          message: this.message,
        });
        (this.name = ""), (this.message = "");
      }
    },
    deleteComment(index) {
      this.comments.splice(index, 1);
    },
  },
  computed: {
    allComments() {
      return this.comments.map((comment) => ({
        ...comment,
        name: comment.name.trim() === "" ? "Anonimo" : comment.name,
      }));
    },
  },
  watch: {
    //observer
    comments(val) {
      console.log("val", val);
    },
  },
};
</script>

    